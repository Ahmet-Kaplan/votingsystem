public class InitPeriodExceptionVS extends ExceptionVS {

    public InitPeriodExceptionVS(String message) {
        super(message);
    }

    public InitPeriodExceptionVS(String message, String metaInf) {
        super(message, metaInf);
    }

    public InitPeriodExceptionVS(String message, String metaInf, Throwable cause) {
        super(message, metaInf, cause);
    }

    public InitPeriodExceptionVS(String message, Throwable cause) {
        super(message, cause);
    }

}
